import { mkdir, readFile, writeFile } from "node:fs/promises";

const promptPath = new URL("../agent/generated/genui-system-prompt.txt", import.meta.url);
const modulePath = new URL("../agent/generated/genui-system-prompt.ts", import.meta.url);

await mkdir(new URL("../agent/generated/", import.meta.url), { recursive: true });
await mkdir(new URL("../shared/genui/", import.meta.url), { recursive: true });

const prompt = await readFile(promptPath, "utf8");
const fencedProtocolPrompt = prompt.replace(
  "Your ENTIRE response must be valid openui-lang code — no markdown, no explanations, just openui-lang.",
  "The content inside the required `openui` fenced block must be valid openui-lang. Do not emit any other prose.",
);
await writeFile(modulePath, `// Generated by pnpm genui:generate. Do not edit manually.\nexport default ${JSON.stringify(fencedProtocolPrompt)};\n`);
